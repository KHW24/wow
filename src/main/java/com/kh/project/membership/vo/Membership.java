package com.kh.project.membership.vo;

public class Membership {
	
	private int mshipGrade;
	private String mshipDate;
	private int mshipPrice;
	
	public int getMshipGrade() {
		return mshipGrade;
	}
	public void setMshipGrade(int mshipGrade) {
		this.mshipGrade = mshipGrade;
	}
	public String getMshipDate() {
		return mshipDate;
	}
	public void setMshipDate(String mshipDate) {
		this.mshipDate = mshipDate;
	}
	public int getMshipPrice() {
		return mshipPrice;
	}
	public void setMshipPrice(int mshipPrice) {
		this.mshipPrice = mshipPrice;
	}
}
	
